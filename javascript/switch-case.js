let input = Math.floor(Math.random() * 10); // returns a random integer between 0 and 10;

switch (input) {
    case 1:
        console.log("One");
        break;
    case 2:
        console.log("Two");
        break;
    case 3:
        console.log("Three");
        break;
    case 4:
        console.log("Four");
        break;
    default:
        console.log("Invalid input");
}